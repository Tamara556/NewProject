package homework1;

public class ForlfExamples {
    public static void main(String[] args) {
        int x;
        for (x = 0; x < 11; x = x + 1)
            System.out.println("x;" + x);
        int y;
        for (y = 50; y > 0; y = y - 1)
            System.out.println("y;" + y);
        int a;
        for (a = 1; a < 20; a = a + 2)
            System.out.println("a;" + a);
    }
}